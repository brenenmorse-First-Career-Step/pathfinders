export default function AdminSidebar() {
    const pathname = usePathname();
    const router = useRouter();
    const { signOut, user } = useAuth();

    const handleSignOut = async () => {
        await signOut();
        router.push('/admin/login');
    };

    const isActive = (href: string) => {
        if (href === '/admin/dashboard') return pathname === href;
        return pathname.startsWith(href);
    };

    return (
        <aside className="w-64 bg-white border-r border-gray-200 min-h-screen flex flex-col shrink-0">
            <div className="p-6 border-b border-gray-200">
                <Link href="/admin/dashboard" className="font-bold text-gray-900">
                    Admin Panel
                </Link>
                <p className="text-xs text-gray-500 mt-1">FirstCareerSteps</p>
            </div>
            <nav className="flex-1 p-4 space-y-1">
                {navItems.map((item) => (
                    <Link
                        key={item.name}
                        href={item.href}
                        className={`flex items-center gap-3 px-4 py-3 rounded-lg transition-colors ${
                            isActive(item.href) ? 'bg-blue-600 text-white' : 'text-gray-700 hover:bg-gray-100'
                        }`}
                    >
                        <svg className="w-5 h-5 shrink-0" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d={item.icon} />
                        </svg>
                        <span className="font-medium">{item.name}</span>
                    </Link>
                ))}
            </nav>
            <div className="p-4 border-t border-gray-200">
                <div className="px-4 py-3 bg-gray-50 rounded-lg mb-2">
                    <p className="text-sm font-medium text-gray-900 truncate">{user?.email}</p>
                </div>
                <button
                    type="button"
                    onClick={handleSignOut}
                    className="w-full flex items-center gap-3 px-4 py-3 text-red-600 hover:bg-red-50 rounded-lg transition-colors"
                >
                    <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                        <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
                    </svg>
                    <span className="font-medium">Logout</span>
                </button>
            </div>
        </aside>
    );
}
